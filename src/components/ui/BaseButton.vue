<template>
  <button class="color" v-if="!link" :class="mode">
    <slot></slot>
  </button>
  <router-link class="color" v-else :to="linkDetail" :class="mode">
    <slot></slot>
  </router-link>
</template>

<script>
export default {
  props: {
    mode: {
      type: String,
      required: false,
      default: null
    },
    link: {
      type: Boolean,
      required: false,
      default: false
    },
    to: {
      type: String,
      required: false,
      default: '/'
    },
    id: {
      type: String,
      required: false,
      default: ""
    }
  },
  computed: {
    linkDetail(){
      return this.to + '/' +this.id
    }
  }
}
</script>

